public class Passenger {

    private String name;
    private int numberBags;

    public Passenger(String name, int numberBags) {
        this.name = name;
        this.numberBags = numberBags;
    }

    public String getName() {
        return this.name;
    }

    public int getNumberBags() {
        return this.numberBags;
    }

}
